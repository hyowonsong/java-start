package Chapter4;

public class GradeSwitchEx {
    public static void main(String[] args) {
        String grade = "B";

        switch (grade){
            case "A":
                System.out.println("탁월한 성과입니다.");
                break;
            case "B":
                System.out.println("좋은 성과입니다.");
                break;
        }




    }

}
